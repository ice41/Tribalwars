						<h1>Biserica</h1>
						<img src=http://help.innogames.de/graphic/staemme/church_f1.png />

<p>Biserica întareste credinta locuitorilor din satele învecinate. Fara biserica, trupele acestui sat si a satelor aflate în raza de influenta a acestei biserici, vor lupta doar cu jumatate din putere. Fiecare din satele tale ar trebui sa fie în raza de influenta a unei biserici. Poti construi numai o singura biserica în fiecare sat. La început ai <a href="?article=church_first">prima biserica</a>. </p>

<p>Biserica poate fi atacata prin catapulte-spre deosebire de prima biserica. Biserica are o rezistenta mai mare împotriva catapultelor, decât orice alta cladire care se afla la acelasi nivel de constructie.</p>

<p>În momentul în care ai mai multe sate, îti mai poti construi o biserica. Aceasta este o cladire foarte scumpa, pentru care ai nevoie de multe locuri în ferma, deci nu trebuie sa o construiesti în fiecare din satele tale. Bisericile noi, construite de tine pot fi marite. Cu cât mai mare nivelul de constructie al bisericii, cu atât mai mare va fi si raza ei de influenta.</p>

<p>În privirea grafica a satului, poti vedea daca satul tau se afla în raza de influenta a unei biserici si câte biserici îti maresc puterea. Pe harta poti vedea raza de influenta a bisericii tale. Cercul albastru arata raza de influenta.</p>

<ul>
<li>Biserica va fi distrusa la cucerirea unui sat.<li>
<li>Un sat necesita doar o singura biserica, pentru a avea puterea completa de lupta. Daca mai construiesti înca o biserica nu-ti vei mari puterea.</li>
<li>Numai biserica ta poate mari puterea de lupta a trupelor tale. Bisericile membrilor tribului tau nu au influenta asupra trupelor tale.</li>
<li>Daca sprijini un jucator, este valabila influenta bisericii lui</li>
<li>La atac este important satul din care vin trupele tale. Daca acesta se afla în raza de influenta a unei biserici, atunci trupele tale vor lupta cu puterea deplina. În cazul acesta este totuna, daca satul atacat se afla în raza de influenta a bisericii tale sau nu.</li>  
</ul>                                   </td>
