						<h1>Paladin</h1>
						<table>
<tr>
<td valign="top"><img src="/graphic/unit_big/knight.png?1" alt="" /></td>
<td>
Fiecare jucator poate avea un paladin. Pentru a recruta un paladin trebuie sa intri la "Statuie". Poti sa-ti redenumesti paladinul, daca doresti. Daca paladinul moare, poate fi refacut la statuie.
<br /><br />
Paladinul este o completare a trupelor tale pentru începutul jocului. El îti micsoreaza pierderile si te poate ajuta si la transportul resurselor.
Paladinul este o completare perfecta pentru trupele tale defensive. Este foarte efectiv, atunci când mai multi prieteni sau membrii ai tribului trimit trupele lor cu paladini spre un jucator. Asa pot fi învinse si armate foarte mari.
Bonusurile obiectelor se aduna. Dar bonusul fiecarui obiect se considera doar o singura data.
<br /><br />
Paladinul are un rol foarte important si pe parcursul jocului, prin faptul ca el mareste viteza trupelor tale de sprijin. Fiecare trupa de sprijin care porneste la drum cu paladinul, va prelua viteza acestuia. De exemplu, luptatorii cu sabia pot înainta cu o viteza dubla fata de viteza lor normala de mars.
Dupa ce vei construi mai multe statui, poti muta paladinul din sat în sat.
