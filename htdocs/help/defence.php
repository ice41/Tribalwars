						<h1>Cum sa te aperi</h1>
						Daca esti atacat, de obicei atacatorul vrea ori resursele tale, ori vrea sa-ti cucereasca satul.<br />
Daca nu exista nobili inclusi în atac, atacatorul încearca probabil sa-ti fure resursele. Exista câteva mici lucruri care le poti face pentru a te apara <br />
<br />
<li>Contacteaza tribul</li>
<li>Pastreaza cantitatile de resurse scazut, folosindu-le imediat ce se produc.</li>
<li>Construieste-ti ascunzatoarea si zidul</li><br />
Toate unitatile au caracteristici diferite, ceea ce le face bune in atac sau mai bune în aparare. Vei gasi mai multe detalii în  <a href="?article=units">Descrierea unitatilor</a>.<br />
La început, paladinul este foarte eficient sa te protejeze la primele atacuri si devine mai valoros cand alt jucator îti trimit paladinul lor ca suport.
