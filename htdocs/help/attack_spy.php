						<h1>Spionajul.</h1>
						<p>Cu ajutorul spionilor se pot obtine informatii despre satele adversarilor. Pentru a spiona un sat pur si simplu se ataca acest sat cu spioni, la fel ca si cu oricare alt tip de unitati.
Dar te rog sa nu uiti, ca un atac victorios necesita cel putin 5 spioni.</p>
<p>Se poate evita spionarea <b>doar</b> prin recrutarea de spioni proprii. Atunci când esti atacat cu spioni, proprii spioni ataca spionii inamici, dar niciunul din spionii tai nu moare.</p>

<br />
Rezultatul unui atac cu spioni depinde de proportia spionilor care supravietuiesc.<br /><br /> 
<li><b>mai putin de 50%</b>, dar cel putin un spion ramas în viata: Se pot vedea doar unitatile care stationeaza în satul respectiv.</li>

<li><b>mai mult 50%</b> din spioni supravietuiesc <b>Spionii sunt nivelul 1</b>: Se pot vedea, de asemenea, si resursele din sat. </li>

<li><b> mai mult 70%</b> din spioni supravietuiesc <b> Spionii sunt nivelul 2</b>: Se pot vedea, de asemenea, si cladirile din sat. </li>

<li><b> mai mult 90%</b> din spioni supravietuiesc <b> Spionii sunt nivelul 3</b>: Se pot vedea, de asemenea, si unitatile care sunt în exteriorul satului.</li><br /> <br />
Pentru a se apara cu succes, în fata unui atac de spionaj, aparatorul trebuie sa aiba în sat un numar de spioni cel putin la fel de mare ca al atacatorului.<br />


