						<h1>Cum sa cuceresti alte sate</h1>
						Înnobilare înseamna cucerirea altui sat. Este singura cale prin care poti avea un al doilea sat. Pentru a înnobila un sat ai nevoie mai intâi de o generatie de nobili, cu care sa ataci tinta. Prin atacuri successive nobilul scade loialitatea. Loialitatea de baza a unui sat este 100 si ea scade cu fiecare atac. 20-35, In acest caz, nu conteaza daca trimiti unul sau mai multi nobil într-un atac. Pentru a prelua un sat trebuie sa-i scazi loialitatea sub 0. In mod normal aceasta se face prin 4-5 atacuri, iar în cazuri rare doar cu 3 atacuri.
Daca trimiti un nobil singur va fi omorât de apararea satului dusman. Trimite întotdeauna însotitori cu el.
<br /> <br />
Imediat ce generatia de nobili a scazut loialitatea sub 0, acesta ia sub stapânire satul respectiv, si nu mai poate fi folosit la alte atacuri.
