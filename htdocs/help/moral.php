						<h1>Moral</h1>
						Moralul protejeaza jucatorii începatori. Acesta se calculeaza pe baza raportului de puncte dintre atacator si aparator.De obicei, se ataca cu moral 100%, dar daca ataci un jucator mult mai mic ca tine, vei fi în dezavantaj. La un atac cu moral 45%, trupele tale ataca doar la 45% din potentialul lor ofensiv.
Daca un jucator cu punctaj mic ataca unul mult mai mare, nu exista bonus pentru atac. Moralul maxim este 100% si nu se primeste nici un bonus în plus ca si un moral de 130%.
<br />
În cadrul simulatorului este disponibil un calculator de moral, unde doar trebuie introduce punctajele celor doi jucatori.
