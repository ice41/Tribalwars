						<h1>Viteza</h1>
						Viteza unitatilor o poti vedea în <a href="?article=units">Trupe</a>. Daca trimiti mai multe unitati împreuna, viteza se va ghida dupa cea mai înceata unitate.
O exceptie face aici paladinul, daca trimiti trupe de sprijin împreuna cu un paladin, atunci trupele vor prelua viteza acestuia.
<br />Trupele folosesc întotdeauna drumul direct, deci merg diagonal peste câmpuri. Lacurile si muntii nu le influenteaza cu nimic directia sau viteza.                                   </td>
