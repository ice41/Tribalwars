						<h1>Apararea de baza</h1>
						Fiecare sat are o aparare de baza. Asta înseamna ca fiecare sat se apara singur, chiar daca nu exista trupe in sat. Astfel, armate de atac mici, spre exemplu unul sau doi lancieri vor muri chiar fara a avea trupe defensive în sat. Apararea de baza este marita prin cresterea zidului.                                   </td>
