<h3> Conditii de atac </h3> 
<ul><li> <b>def</b> = Armata defensiva 
</li><li> <b>off</b> = Armata ofensiva
</li><li> <b>sat defensiv </b> = Un sat folosit pentru recrutarea de armata defensiva 
</li><li> <b>Sat ofensiv</b> = Un sat folosit pentru recrutarea de armata ofensiva 
</li><li> <b>farmare/jefuire/furt</b>= Atacul asupra altor sate pentru a lua resurse 
</li><li> <b>curatare</b> = a omorî toate trupele dintr-un sat
</li><li> <b>asediu</b> = atacuri repetate asupra unei tinte, pentru a o împiedica sa se dezvolte sau pentru înnobilare 
</li><li> <b>înnobilare</b> = Asimilarea satului altui jucator, ca urmare a atacurilor repetate cu nobili, prin scaderea adeziunii la 0 
</li><li> <b>pre-înnobilare</b> = Coborârea loialitatii unui sat, prin atacuri repetate cu propriile generatii de nobili, pâna la punctul în care este nevoie de un singur atac, din partea altui jucator, pentru înnobilarea satului (toate acestea ca urmare a distantei mari) 
</li><li> <b>Fake-uri</b> = Trimiterea a doar unei unitati, pentru derutarea adversarului 
</li><li> <b>sincronizare</b> = Trimirea de mai multe atacuri, asupra unuia sau mai multor jucatori, astfel momentul de sosire sa fie acelasi 
</li><li> <b>sinucidere</b> = omorârea propriilor trupe pentru a elibera spatiu din ferma, ori cu scopul de a ceda satul  
</li><li> <b>blocare nobil</b> =  Sincronizarea defensivei unui sat astfel încât trupele de curatare ajungîn sat înaintea trupelor de suport, iar nobilul dupa suportul defensiv
</li><li> <b>Spike</b> = Sustinerea unui sat abandonat sau care este curatat de trupe, cu scopul inducerii de pierderi asupra jucatorilor care ataca satul respectiv. 
</li><li> <b>centru</b> = cercul interior al hartii </li><li> <b>rim (marginea hartii)</b> = cercul exterior al hartii 
</li><li> <b>100/100/50</b> = 100 Sulite/ 100 Spade/ 50 Cavalerie grea (Versiunea scurta a unei cereri de suport defensiv) 
</li><li> <b>Nivele</b> = Nivele tehnologice ale unitatilor 
</li><li> <b>Snipuire</b> = Trimiterea unei generatii de nobili pentru a înnobila un sat, care tocmai a fost cucerit sau care este în proces de înnobilare, de catre alt jucator 
</li><li> <b>Sistem de avertizare /Tripwire</b> = O retea de jucatori care îsi trimit reciproc suport, pentru a fi la curent cu atacurilor asupra lor, de îndata ce aceastea se produc 
</li><li> <b>PNA</b> = Pact de Neagresiune </li></ul> 
