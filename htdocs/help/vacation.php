						<h1>Mod de vacanta</h1>
						Daca nu ai timp o perioada sa fi activ pe triburile, este recomandabil sa-ti gasesti un loctiitor, care sa-ti administreze contul cât timp lipsesti. Pentru aceasta, intra în "Setari" -> "Mod de vacanta" si adauga numele loctiitorului. Loctiitorul trebuie sa fie o persoana de încredere, pentru ca-ti poate fi si daunator.
De îndata ce puteti, va luati contul înapoi si puteti înceta loctiirea.<br />
<br />
<br />
Atunci când sunteti solicitat sa administrati contul altcuiva, trebuie, mai înainte de toate, sa acceptati cererea de loctiire (care poate fi si refuzata, bineînteles), de la Setari – Mod de vacanta. De îndata ce cererea de loctiire a fost acceptata, va fi vizibil un link “Login”, prin care va puteti loga în cont.<br />
