						<h1>Rapoarte</h1>
						Rapoartele contin multe informatii folositoare. Vei primi rapoarte de comert, atacuri, invitatii în alte triburi, precum si altele.<br /><br />
Rapoartele sunt sortate pe categorii, în mod automat.
<br />Rapoartele atacurilor, ca aparator si atacator au diferite culori; în continuare, sunt date explicatii despre diferite cazuri care pot apare:<br /><br />
<img src="/graphic/dots/green.png" alt="" /> În cadrul luptei, nu s-a produs nici o pierdere<br />
<img src="/graphic/dots/yellow.png" alt="" /> Ai câstigat batalia, dar ai suferit pierderi<br />
<img src="/graphic/dots/red.png" alt="" /> Ai pierdut batalia.<br />
<img src="/graphic/dots/blue.png" alt="" /> A fost un atac de spionaj.<br /> <br />
Câteodata, nu se doreste primirea unui anume fel de rapoarte. Pentru aceasta, se poate seta un filtru. În sectiunea Rapoarte, se regaseste o optiune "Filtru". O data ce filtrul este activat, nu se va vizualiza categoria de rapoarte selectata.<br />
<br />
Rapoarte sunt sterse în mod automat, dupa o perioada de timp. Daca doresti sa pastrezi rapoartele o perioada mai mare de timp, acestea trebuie arhivate. Aceasta optiune este disponibila doar în cazul unui Cont Premium.<br /> <br />
