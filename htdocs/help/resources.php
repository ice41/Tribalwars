						<h1>Resurse</h1>
						<p>Pentru a antrena trupe, ai nevoie de resurse. Primesti automat resurse, ca urmare a productiei minelor (de lemn, argila si fier), chiar daca nu esti logat pe cont.</p>
<p>Cantitatea de resurse depinde de marimea cladirilor de resurse. Este important sa construiesti magazia la un nivel mare, pentru a fi sigur ca resursele nu se irosesc. În dreapta sus se poate vedea cantitatea de resurse ce poate fi stocata, precum si cantitatea curenta existenta în magazie.</p>

<img src=http://help.innogames.de/graphic/staemme/storage.png />
Numarul din dreapta arata câte resurse pot fi stocate, din fiecare categorie. În acest exemplu, numarul 1.000 înseamna ca o 1,000 lemn, 1,000 argila, 1,000 fier pot fi stocate. Numarul din stânga arata câte resurse sunt stocate în acest moment.                                   </td>
