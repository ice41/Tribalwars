						<h1>Recruteaza unitati</h1>
						Iti poti recruta primele unitati în cazarma. Pentru a construi cazarma trebuie sa-ti maresti cladirea principala la nivelul 3 <br />
La început vei avea posibilitatea de a recruta doar lancieri, pentru a recruta si alte unitati ai nevoie de fierarie sau alte cladiri (cum ar fi grajdul sau atelierul)
În <a href="?article=units">Descrierea unitatilor</a> Vei gasi o descriere scurta despre cerintele anumitor unitati si care sunt însusirile lor.<br /><br />

