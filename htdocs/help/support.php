						<h1>Cum sa trimiti suport defensiv</h1>
						Se întâmpla adeseori ca un coleg de trib sa aiba nevoie de ajutorul tau.  Îi poti trimite acestuia resurse sau suport defensiv. Pentru acesta, trebuie sa te duci la Piata centrala, unde sa introduci coordonatele satului si numarul de trupe pe care doresti sa le trimiti ca suport. Dupa care da click pe "Suport" si "Ok", pentru confirmare.  <br />
Când trimiti paladinul împreuna cu armata de sprijin, toate trupele tale se vor deplasa cu viteza paladinului.
<br />
Când trupele tale de sprijin sunt atacate în satul altui jucator, vei primi un raport, incluzând numarul trupelor pierdute. Nu poti vedea cine si cu ce a atacat. Când trupele tale nu mai sunt necesare în acel sat, ele pot fi retrase. Pentru aceasta, click pe piata centrala si apoi pe "trupe". Acolo poti retrage o parte din ele, sau pe toate.<br />
