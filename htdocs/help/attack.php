						<h1>Cum sa ataci</h1>
						De îndata ce ai antrenat câteva unitati, poti începe atacurile asupra celorlati jucatori. Intra pe harta si alege un sat. Selecteaza "trimite trupe". Este important sa nu scapi din vedere faptul ca fiecare sat are propria aparare de baza, precum si posibilitatea ca în satul atacat sa stationeze trupe. <br />
Toate unitatile au caracteristici diferite, ceea ce le face bune in atac sau mai bune în aparare. Vei gasi mai multe detalii în  <a href="?article=units">Descrierea unitatilor</a>. <br />
