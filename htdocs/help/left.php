						<h1>Sat parasit</h1>
						<p>Satele parasite la poti recunoaste dupa numele " Sat parasit". In modul de lupta acestea se comporta la fel ca si satele jucatorilor. Pentru ca aceste sate nu mai au stapân, au moralul întotdeauna 100%. Pentru satele parasite nu este valabil bonusul de noapte. De cucerit trebuie sa le cuceresti ca si orice alt sat. Aceste sate cresc singure cu timpul.</p>

<p>
Daca un jucator este inchis din cauza nerespectarii regulilor satele lui se vor transforma in sate parasite. Câteodata aceste sate ofera celor din prejur avantaje. În cazul acesta se posteaza trupe în ele.
