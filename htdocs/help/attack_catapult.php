						<h1>Catapulte</h1>
						Catapultele pot distruge cladirile tinta. In acest caz, poti alege ce cladire sa fie atacata. Poti folosi simulatorul pentru a estima distrugerile facute de catapulte. In acest caz, nu conteaza ce cladire va fi atacata, ci doar nivelul ei. Asta înseamna ca ai nevoie de acelasi numar de catapulte pentru a distruge o curte nobila, care are nivelul 3, cât ai nevoie sa distrugi fieraria de la nivelul 3.
Urmatoarele cladiri nu pot fi demolate mai jos de nivelul 1: Cladirea principala, ferma, magazia.
Ascunzatoarea nu poate fi atacata cu catapulte.<br />
