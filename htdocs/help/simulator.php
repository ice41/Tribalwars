						<h1>Simulator</h1>
						Simulatorul îti permite sa estimezi rezultatul unei lupte viitoare. Pentru acesta, se introduc în simulator trupele care vor participa la lupta din partea atacatorului si a aparatorului.
zidul si moralul
Dupa acestea, simulatorul calculeaza rezultatele luptei si afiseaza pierderile.
Poti, de asemenea, sa modifici norocul. Întotdeauna e bine sa calculezi ce se întâmpla în cel mai rau caz posibil (si anume, moral -25%).<br />
