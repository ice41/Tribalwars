<h4>§8)  Stergerea contului / ban</h4>
<p>Atunci când jucati Triburile trebuie sa respectati Regulile Jocului. Staff-ul jocului si moderatorii îsi rezerva dreptul de a închide conturi/a da pedepse oricarui jucator care nu respecta Regulile Jocului. Va rugam sa urmati întocmai orice rugaminte sau îndrumare primita din partea staff-ului tehnic. Nerespectarea acestora nu va fi trecuta cu vederea, si poate rezulta în ban-uri sau închideri permanente de conturi. Echipa Team Triburile supravegheaza jocul, sanctionând orice încalcarea a regulamentului.
Daca nu sunteti sigur de legalitatea faptelor ce le veti face, va rugam sa depuneti un ticket înainte de a savârsi actiunea. Echipa va va oferi informatiile necesare. Situatia conturilor blocate se discuta doar între Echipa Team Triburile si jucatorul/jucatorii în cauza. 
Un cont poate fi modificat, închis sau sters din motive diferite. Stergerea poate urma în mod automat, daca în ultimele 14 zile contul nu a fost folosit. Conturile banate se sterg automat prin sistem dupa 14 zile, chiar daca au cont premium activ sau mai au puncte premium. În cazuri extreme, conturile pot fi interzise pe toate lumile.
