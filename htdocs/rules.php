<h4>§4)  Comunicare</h4>
<p>Triburile este versiunea româneasca a jocului TribalWars. De aceea va rugam sa folositi pe acest server doar limba româna pentru a fi înteles de catre toti jucatorii! Toate profilurile, descrierile, adaugarile de joc si forumul trebuie sa fie scrise in româna. Nu este permis sa fie întemeiate triburi exclusiv numai pentru o nationalitate sau limba vorbita , fapt care ar exclude alti jucatori din cauza nationalitatii lor. Nu se vor acorda raspunsuri de la suport în limbi straine. Nerespectarea repetata a acestor reguli duce la stergerea contului. Daca în timpul jocului se scrie un mesaj în alta limba decât româna vom lua masuri si jucatorii vor primi pedepse. Jigniri împotriva altor jucatori sunt interzise. Amenintari si santajari au voie sa fie numai în joc. Afirmatii politice extreme, pornografie si alte afirmatii ilegale în joc sunt interzise.
Este interzisa trimiterea de mailuri sau de linkuri cu scopul de a fura idei din joc, deasemenea este interzisa folosirea jocului ca reclama.<br />
<br />
Este interzis a se posta copia totala sau partiala a unui mesaj privat primit în joc, sau în orice sistem de mesagerie,care contine insulte, jigniri sau acuze aduse echipei de admini.
Postarea discutiilor purtate cu membrii echipei de suport (ticketele) fara acordul scris al adminului respectiv, se sanctioneaza direct cu ban.
Acuzatiile nefondate aduse echipei de admini ( atât pe forumul extern cât si catre suport), pot duce la excluderea din joc a respectivilor jucatori.
 Orice ati scrie poate fi folosit ca mijloc de proba împotriva dumneavoastra în cadrul unei contestatii. Noi nu vom accepta daca mai târziu veti spune ca a fost o gluma.<br />

<div id="more_4" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Este permisa numirea unui jucator începator ca (Noob).</li>
 <li>Este interzisa jignirea unui jucator.</li> 
 <li>Este interzis, santajul unui jucator pentru puncte Premium.</li>
 <li>Este interzisa numirea unui trib NSDAP sau KKK.</li>
 <li>Este interzisa introducerea de linkuri pornografice în profil.</li>
 <li>Nume ca "Adolf Hitler" sau "Violator de fetite" duc la excluderea directa din joc.</li>
 </ul>

