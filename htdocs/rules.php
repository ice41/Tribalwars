<h4>§1)  Un cont pro jucator</h4>
<p>Fiecarui jucator îi este permis sa administreze un singur cont pe lume de joc. Exceptie fac conturile care sunt administrate ca sitter, prin intermediul optiunii Mod de Vacanta. Transmiterea parolei unui alt jucator este interzisa
<br />

<div id="more_1" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Este permis sa joace mai multi utilizatori pe un singur calculator. În schimb, jucatorilor le este permisa logarea numai pe contul lor, nu si pe al celorlalti membrii ai familiei, pe al prietenilor, si trebuie respectate regulile specificate la § 2. Fiecare jucator trebuie sa-si aleaga propria parola, si nu are voie sa o spuna altor jucatori.</li>
 <li>Nu aveti voie sa va logati pe un alt cont folosind parola jucatorului respectiv. Pentru a va putea loga pe alte conturi, trebuie sa fie folosita optiunea de Mod de Vacanta ( Setari -> Mod de Vacanta). Nu va fi tolerata absolut nicio abatere de la aceasta regula.</li>
 <li>Este strict interzisa încercarea de autentificare neautorizata pe alte conturi. Incercari de spargere de conturi vor fi pedepsite.</li>
 <li>Ghicirea parolei unui alt jucator este de asemenea interzisa. Va rugam sa va alegeti o parola greu de ghicit (Evitati parole de genul 123456, numele dumneavoastra etc). Noi nu va vom returna trupele sau satele pierdute în cazul în care cineva a intrat pe contul dumneavoastra pentru ca parola a fost usor de ghicit.</li>
</ul>

