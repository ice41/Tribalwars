<h4>§3)  Mod de vacanta (loctiitor)</h4>
<p>Modul de vacanta este o metoda <b>temporara</b> de a avea grija de contul unei persoane atâta timp cât persoana respectiva nu poate intra pe propriul cont. Câta vreme un jucator este loctiitor pentru alt jucator, nici un fel de interactiuni (atac/ aprovizionare/ sprijin/ atacuri coordonate sau suport) nu sunt permise între oricare dintre conturile jucate de pe legatura de internet a loctiitorului (incluzând prieteni, familie sau colegi cu care acesta împarte legatura de internet). Aceasta regula include toate conturile, inclusiv conturile la care aceste persoane sunt loctiitori.<br />
<b>Toate interactiunile sunt interzise pâna ce trec 24 de ore de la încetarea modului de vacanta.</b><br />
Nu este permisa tinerea unui cont în mod de vacanta si folosirea acestuia pentru sustinerea altor conturi. Conturile tinute în mod de vacanta si folosite doar pentru a sustine alte conturi vor fi blocate/închise.

Toate mentiunile facute la punctul §2 trebuie respectate.<br />
Jucatorii si loctiitorii acestora sunt responsabili în egala masura pentru actiunile întreprinse în modul de vacanta. Asigurati-va ca aveti încredere în persoana desemnata ca loctiitor sau în persoana al carui loctiitor urmeaza sa fiti.<br />
Nu este permis abuzul comis asupra contului avut în mod de vacanta. Loctiitorii care intentionat distrug sau pagubesc grav un cont avut în mod de vacanta vor fi pedepsiti.<br />
Cu toate acestea, în timp ce pedepsim loctiitorul daca observam intentia sa clara de a distruge munca celui pe care îl înlocuiesc, contrar instructiunilor lasate, <b>nu putem repara</b> prejudiciul adus acelui cont.<br /><br />

<div id="more_3" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Este permisa atacarea si cucerirea de sate de pe contul avut în mod de vacanta.</li>
 <li>Nu este niciodata permisa daruirea resurselor de pe conturile aflate în mod de vacanta, chiar daca acele sate au suficiente resurse.</li>
 <li>Este permisa atacarea diversilor jucatori ai aceluiasi trib de pe contul loctiitorului si contul avut în mod de vacanta ( adica atacuri separate pe jucatori diferiti).</li>
 <li>Este interzis sa se dea mesaj unui jucator pentru a afla daca acesta este pe cont în scopul de a ataca mai târziu cu un alt cont de pe aceeasi legatura de internet.</li>
 <li>Nu este permisa folosirea unui cont în beneficiul oricarui alt cont de pe aceeasi legatura de internet.</li>
 <li>Un loctiitor nu poate ataca acelasi jucator de pe contul sau si de pe contul avut pe mod de vacanta atâta timp cât modul de vacanta este activ. Aceasta actiune este ilegala chiar daca între atacurile date sunt mai mult de 24 de ore distanta. Acelasi jucator poate fi atacat/sprijinit numai dupa ce de la încheierea modului de vacanta au trecut minimum 24 de ore.</li>
 <li>Este interzis suportul sau atacul asupra unui jucator ce împarte aceeasi legatura de internet cu loctiitorul.</li>
 <li>Nu este permisa atacarea nici unui jucator aflat în mod de vacanta la unul din jucatorii care împart aceeasi legatura de internet.</li>
 <li>Este strict interzis atacul sau suportul aceluiasi jucator de pe contul loctiitorului si contul avut în mod de vacanta.</li>
</ul>
<h4>Modul de vacanta pe termen lung</h4>
Permitem jucatorilor sa fie loctiitori pe termen lung cu restrictii asupra contului avut în mod de vacanta. Daca un cont are un proprietar ce se întoarce si stiti când acesta se întoarce, este în regula sa fiti loctiitor. Conturile avute în mod de vacanta pe care proprietarii nu se mai întorc sunt considerate <b>"Mod de vacanta permanent"</b>. Cum este temporar, daca un cont are loctiitor 60 de zile din ultimele 120 de zile, contul devine cont „limitat”. Loctiitorul nu va putea sa atace sau sa sprijine alti jucatori sau sa trimita resurse de pe contul respectiv. Fiecare cont trebuie sa aiba un jucator activ. Daca proprietarul contului decide ca el\ea nu se mai întoarce pe cont, contul respectiv nu poate fi jucat de pe mod de vacanta permanent de un loctiitor.<br />
Toate mentiunile de la punctele §2 si §3 trebuie urmate.<br />
<ul>
<li>Este interzisa jucarea permanenta de pe mod de vacanta în beneficiul altor conturi, chiar daca aceste conturi sunt pe o legatura de internet distincta.<br />
<li>Orice cont ce depaseste 120 de zile de mod de vacanta fara motiv, va avea modul de vacanta încetat.<br />
</ul>

