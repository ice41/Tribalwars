<h4>§2)  Mai multi jucatori cu aceasi legatura de internet</h4>
<p>În timp ce jucatorii împart o conexiune la internet (si 24 de ore dupa aceea) nu este permisa absolut nici o interactiune între conturi. Nu sunt permise transferurile de resurse, sa-si trimita suport sau atac reciproc, atacuri coordonate sau orice alta interactiune între conturi. împartirea conexiunii trebuie anuntata (setari -> împartirea conexiunii) si orice interactiune între conturi este interzisa. Pe Speedserver (SDS) este interzis, ca mai multe conturi sa fie jucate de pe un singur computer.
<br />

<div id="more_2" style="display: none">	<h5>Explicatii detaliate:</h5>Regula este pentru toti utilizatorii care joaca pe acelasi computer sau în acelasi network. Prin network se întelege joc în aceeasi casa, acelasi loc de munca sau scoala unde computerele sunt legate între ele. Toti jucatorii care folosesc o retea trebuie sa declare acest lucru la împartirea conexiunii
<h4>Exemple:</h4>
<ul>
 <li>Este interzis sa folositi un cont pentru a aduce profituri de orice fel altui cont care foloseste aceeasi retea.</li>
 <li>Este interzis ca jucatorii ce folosesc aceeasi retea sa atace, trimita suport sau resurse unul altuia</li>
 <li>Este interzis ca jucatorii ce folosesc aceeasi retea sa atace, trimita suport sau resurse unui al treilea jucator. Jucatorilor care folosesc aceeasi legatura de internet nu le este admis sa sprijine sau sa livreze resurse catre un al treilea jucator care nu foloseste aceeasi legatura de internet. Folosirea unui cont sau a mai multor conturi intermediare pentru transfer de resurse între 2 conturi ce împart aceeasi legatura la internet este interzisa. Toate conturile implicate incalca regulamentul.</li>
 <li>Jucatorii cu o legatura comuna de internet au voie sa atace acelasi trib.</li>
 <li>Este interzisa utilizarea unui cont în beneficiul unui alt cont ce împarte aceeasi legatura la internet.</li>
<li>Declararea împartirii legaturii de internet nu deroga de la respectarea întocmai a §1)</li>
</ul>

</div>

