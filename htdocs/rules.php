<h4>§10) Conturi de sustinere (push accounts)</h4>
<p>Nu este permisa crearea unor conturi doar pentru ajutorarea unui alt cont sau tinerea conturilor inactive cu acest scop(farmarea conturilor proprii). Conturi folosite doar pentru sustinerea altora vor fi închise.
Conturile care sunt ajutate vor fi de asemenea închise. Daca dumneavoastra considerati ca un cont este folosit doar pentru a ajuta alt cont, va rugam sa raportati acest lucru imediat prin intermediul suportului. Daca primiti resurse de la un cont de sustinere trebuie sa raportati acest lucru, altfel veti primi si dumneavoastra ban alaturi de contul de sustinere.<br />

<div id="more_10" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Daca va decideti sa parasiti jocul, nu aveti voie sa jucati pe cont doar pentru a trimite resurse altor jucatori. Folosirea sitter-ului pentru a face aceasta este de asemenea interzisa si va rezulta într-un ban pentru sitter cât si pentru jucatorul care renunta la joc.</li>
