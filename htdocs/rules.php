<h4>§5) Vânzarea si cumpararea de conturi si obiecte ingam</h4>
<p>Nu este de dorit folosirea conturilor doar în scopuri comerciale. De aceea, este interzisa cumpararea sau vânzarea conturilor, a satelor sau materiilor prime. Totodata este interzis schimbul de resurse între lumi diferite. Este interzisa crearea unui cont având ca singur scop sustinerea altui jucator (resurse si armata).
 Daca decideti sa parasiti jocul, este interzis sa continuati sa jucati doar pentru sustinerea altui cont. De asemenea, utilizarea unui loctiitor permanent sau temporar pentru acest scop este interzisa. În cazul în care primiti resurse de la un cont de împingere, sunteti obligati sa raportati administratorilor acest fapt. Nu este considerat cont de împingere jucatorul activ care participa la diverse forme de suport organizat conform strategiilor tribului
<br />

<div id="more_5" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Este interzisa vânzarea de materii prime prin ebay.</li>
 <li>Este de asemena interzisa vânzarea dreptului de joc sau a licentei contului propriu.</li>
 <li>Este interzisa oferirea contra bani de materii prime sau puncte premium în profil.</li>
 <li>Este interzisa oferirea sau acceptarea unor afaceri personale în schimbul punctelor premium.</li>
 <li>Este interzisa oferta de materii prime din lumea 6 in schimbul resurselor din lumea 7.</li>
</ul>

