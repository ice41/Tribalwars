<h4>§7)  Bots</h4>
<p>Jocul are voie sa fie jucat numai prin browserul normal. Bots sau alte programe, care se logeaza automat sau automatizeaza actiunile jocului sunt strict interzise.<br />
Prin "Meniul rapid" poti da comenzi de JavaScript. Aceste scripturi au voie sa completeze formulare (de ex. trupele care trebuie recrutate in cazarma) si au voie sa declanseze maximum o actiune pro Clic (de ex. Recrutare de trupe).Sunt acceptate doar scripturile aprobate de echipa de administrare din sectiunea <a href="http://forum.triburile.ro/showthread.php?t=13903"><b>Colectie de Scripturi</b><a> .
Scripturile ce utilizeaza Greasemonkey sunt strict interzise.
Scripturile, care reproduc facilitatile unui Cont premium sunt strict interzise.<br />
<br />

<div id="more_7" style="display: none">	<h4>Exemple:</h4>
<ul>
 <li>Este permisa folosirea de unelte de ajutor aflate aici :www.twstats.com </li>
<li>Sunt permise scripturile aprobate de Echipa de Administrare in sectiunea special creata</li>
 <li>Este interzisa folosirea unui program, care poate trimite automat unitatile la drum.</li>
  <li>Este permisa trimiterea circularelor automate care contin informatii despre trib.</li>
 <li>Este interzisa folosirea unui script care pune la dipozitie un "Meniu rapid" chiar daca nu ai Cont Premium.</li>
 <li>Este interzisa folosirea unui script de Meniu rapid care îti permite trimiterea mai multor atacuri printr-un singur clic.</li>
 <li>Este interzisa crearea programelor ce trimit informatii despre activitatea contului dumneavoastra pe alte conturi în caz de atacuri sau orice alte evenimente.</li>
 <li>Este interzisa adaugarea de informatii pe harta sau în alte zone.</li>
 <li>Este interzisa utilizarea unor programe pentru creearea de shortcut-uri care sa schimbe navigarea obisnuita a site-ului.</li>
 <li>Este interzisa downloadarea informatiei despre lume mai mult de 1 data/ora.</li>
</ul>


