<p style="font-weight:bold; margin-top:10px;">Regras de banimento</p>
<ol>
	<li style="margin-top:5px;">Ao se aplicar um ban, deve-se tomar em conta os seguintes princ&iacute;pios:
		<ul>
			<li>BOM SENSO. Bom senso para se medir o ban.</li>
			<li>Motivos para se aplicar um Ban em um jogador, deve-se tomar em conta, a gravidade da infra&ccedil;&atilde;o. Infra&ccedil;&otilde;es para se tomar Ban:
				<ul>
					<li>Modos expressivos (Regra n&ordm; 4), comercializa&ccedil;&atilde;o de contas (Regra n&ordm; 5), Bots e ferramentas ilegais (Regra n&ordm; 8), fraudes (Regra n&ordm; 14).</li>
				</ul>
			</li>
			<li>Motivos para se aplicar um <strong>Ban Global</strong> em um jogador, deve-se tomar em conta, a  gravidade da infra&ccedil;&atilde;o. Infra&ccedil;&otilde;es para se tomar Ban Global:
				<ul>
					<li>Multi contas (Regra n&ordm; 1), tentativa de roubo ou hacking (Regra n&ordm; 6), uso de bugs (Regra n&ordm; 7), passar-se por membro ou antigo membro da equipe (Regra n&ordm; 15).</li>
				</ul>
			</li>
		</ul>
	</li>
</ol>